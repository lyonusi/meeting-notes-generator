import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";

const production = process.argv[2] === "production";

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
Source code is in the src/ directory.
*/`;

// Node core modules are available at runtime in Obsidian's Electron renderer
// (used by the AWS credential file reader). Externalize both bare and "node:"
// scheme names so they resolve at runtime rather than being bundled.
const nodeExternals = [...builtins, ...builtins.map((m) => `node:${m}`)];

const context = await esbuild.context({
  banner: { js: banner },
  entryPoints: ["src/main.ts"],
  bundle: true,
  // Browser platform: required for onnxruntime-web (Whisper WASM) to use its
  // browser code paths instead of node worker_threads/fs, which crash inside
  // the Electron renderer. We dropped the AWS SDK (which needed node platform)
  // in favour of a small Web Crypto SigV4 signer, so browser platform is safe.
  platform: "browser",
  external: [
    "obsidian",
    "electron",
    "@codemirror/autocomplete",
    "@codemirror/collab",
    "@codemirror/commands",
    "@codemirror/language",
    "@codemirror/lint",
    "@codemirror/search",
    "@codemirror/state",
    "@codemirror/view",
    "@lezer/common",
    "@lezer/highlight",
    "@lezer/lr",
    ...nodeExternals,
  ],
  // Aliases for transformers.js running in Obsidian's Electron renderer:
  // - onnxruntime-node -> onnxruntime-web: it detects "node" and imports the
  //   native ONNX runtime; we ship the WASM one instead.
  // - sharp -> empty shim: an optional native image dep we never use for audio;
  //   aliasing avoids a failing runtime require('sharp').
  alias: {
    "onnxruntime-node": "onnxruntime-web",
    sharp: "./build-shims/empty.cjs",
  },
  format: "cjs",
  target: "es2020",
  logLevel: "info",
  sourcemap: production ? false : "inline",
  treeShaking: true,
  outfile: "main.js",
  define: {
    "process.env.NODE_ENV": production ? '"production"' : '"development"',
    // transformers.js computes a "local" path from `import.meta.url`, which is
    // undefined in a CJS bundle and crashes `fileURLToPath`. Define it to a
    // dummy file URL so that call succeeds; we force remote models at runtime
    // (env.allowLocalModels = false), so the computed local paths are unused.
    "import.meta.url": '"file:///obsidian-plugin/main.js"',
  },
});

if (production) {
  await context.rebuild();
  process.exit(0);
} else {
  await context.watch();
}
